// Little Matrix Multiplication
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a, b, c, d, e, f, g, h;
    cin >> a >> b >> c >> d >> e >> f >> g >>h;

    cout << a*e + b*g << " ";
    cout << a*f + b*h << endl;
    cout << c*e + d*g << " ";
    cout << c*f + d*h << endl;

    return 0;
}
